# Suma (+)
print(5 + 3)   # 8

# Resta (-)
print(10 - 4)  # 6

# Multiplicación (*)
print(6 * 2)   # 12

# División normal (/)
print(7 / 2)   # 3.5 → siempre devuelve float

# División entera (//)
print(7 // 2)  # 3 → solo la parte entera

# Módulo (%) → residuo de la división
print(7 % 2)   # 1

# Exponente (**)
print(2 ** 3)  # 8 → 2 elevado a la 3